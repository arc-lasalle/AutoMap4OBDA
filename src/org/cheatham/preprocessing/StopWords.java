	public static String removeStopwords(String input) {
		
		if (stopWords == null || stopWords.size() == 0) {
			init();
		}
		
		input = Preprocessing.stringTokenize(input, true);
		
		for (String s: stopWords) {
			
//			if (input.contains(" " + s + " ") || input.startsWith(s + " ") || input.endsWith(" " + s)) {
//				System.out.println("removing " + s + " from " + input);
//			}
			
			input = input.replaceAll(" " + s + " ", " ");
			
			if (input.startsWith(s + " ")) {
				input = input.substring(s.length());
			}
			
			if (input.endsWith(" " + s)) {
				input = input.substring(0, input.lastIndexOf(s));
			}
		}
		
		return input.trim();
	}
